|
| 參考：
| - BCP 47 標準：語言小寫、文字系統首字母大寫（zh-Hant）
| - Laravel 官網：底線格式（en_GB、zh_TW）
| - SEO 趨勢：使用文字系統而非地區（zh_Hant 而非 zh_TW）
